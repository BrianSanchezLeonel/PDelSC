function suma(n1, n2){
    return n1 + n2;
}

function resta(n1, n2){
    return n1 - n2;
}

function multiplicacion(n1, n2){
    return n1 * n2;
}

function division(n1, n2){
    return n1 / n2;
}

console.log("La suma entre 4 y 5: " + suma(4, 5));
console.log("La resta entre 3 y 6: " + resta(3, 6));
console.log("La multiplicacion entre 2 y 7: " + multiplicacion(2, 7));
console.log("La division entre 20 y 4: " + division(20, 4));